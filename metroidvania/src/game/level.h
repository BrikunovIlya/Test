#pragma once
#include <vector>

/**
 * @brief Класс, представляющий игровой уровень.
 * 
 * Класс Level управляет картой уровня, включая генерацию тестового уровня, 
 * доступ к тайлам и их модификацию.
 */
class Level {
public:
    /** @brief Двумерный массив, представляющий карту уровня. */
    std::vector<std::vector<int>> map;
    /** @brief Ширина уровня в тайлах. */
    int width = 60;
    /** @brief Высота уровня в тайлах. */
    int height = 30;

    /**
     * @brief Конструктор класса Level.
     * 
     * Инициализирует уровень с заданными размерами и генерирует тестовый уровень.
     */
    Level();

    /**
     * @brief Генерирует тестовый уровень с границами, платформами и объектами.
     */
    void GenerateTestLevel();

    /**
     * @brief Получает значение тайла в указанных координатах.
     * @param x Координата x тайла.
     * @param y Координата y тайла.
     * @return Значение тайла (1 для стены, 2 для шипов и т.д.) или 1, если координаты вне карты.
     */
    int GetTile(int x, int y) const;

    /**
     * @brief Устанавливает значение тайла в указанных координатах.
     * @param x Координата x тайла.
     * @param y Координата y тайла.
     * @param value Новое значение тайла.
     */
    void SetTile(int x, int y, int value);
};